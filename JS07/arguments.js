function double(number){
    const doubled = number * 2;
    console.log(number + " Double is " + doubled);
}

double(12);


function difference(num1 , num2){
    const diff = num1 - num2;
    console.log(num1 , num2 + " difference is " + diff)
}

const fatherAge = 50;
const sonAge = 21;

difference(fatherAge, sonAge)